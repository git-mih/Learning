# First-class functions:

# an object is a first-class object if it has the following caracteristics:
#   - can be passed to a function as argument;
#   - can be returned from a function;
#   - can be assigned to a variable;
#   - can be stored into a data structure such as, list, tuple, dict, etc.

# any object that have all these caracteristics is called a first-class object essentially.
# and function objects does have it.

